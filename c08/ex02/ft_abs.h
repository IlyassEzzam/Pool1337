#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(Value) ({ \
		int num; \
		if (Value < 0) \
		{ \
		num = -Value; \
		} \
		else \
		{ \
		num = Value; \
		} \
		num; \
		})

#endif
